    public static Color getMachineColor(MachineType type) {
        switch (type) {
            case Bassline:
                return Color_Bassline;
            case BeatBox:
                return Color_Beatbox;
            case EightBitSynth:
                return Color_BitSynth;
            case FMSynth:
                return Color_FMSynth;
            case KSSynth:
                return Color_KSSynth;
            case Modular:
                return Color_Modular;
            case Organ:
                return Color_Organ;
            case PCMSynth:
                return Color_PCMSynth;
            case PadSynth:
                return Color_PadSynth;
            case SubSynth:
                return Color_SubSynth;
            case Vocoder:
                return Color_Vocoder;
        }
        return null;
    }
